
# 덧셈
def addition(x, y):
    return x + y

# 뺄샘
def subtraction(x, y):
    return x - y


# 곱셈
def multiplication(x, y):
    return x * y
 

# 나눗셈
def division(x, y):
    return x / y
 

# 거듭제곱
def power(x, y):
    return x ** y
 

# 나머지(모듈러)
def modular(x, y):
    return x % y
 

# 비트 AND 연산
def bitAND(x, y):
    return x & y
 

# 비트 OR 연산
def bitOR(x, y):
    return x | y
 

# 비트 XOR 연산
def bitXOR(x, y):
    return x ^ y
 